/**
 * Get sequence by name
 */
export function getSequence(name) {
  return allSequences[name] || null;
}

/**
 * Get all sequence names
 */
export function getSequenceNames() {
  return Object.keys(allSequences);
}

/**
 * Get sequences by duration range
 */
export function getSequencesByDuration(minSeconds, maxSeconds) {
  const minMs = minSeconds * 1000;
  const maxMs = maxSeconds * 1000;

  return Object.values(allSequences).filter(
    seq => seq.duration >= minMs && seq.duration <= maxMs
  );
}

